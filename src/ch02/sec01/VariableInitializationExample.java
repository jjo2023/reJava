package ch02.sec01;

public class VariableInitializationExample {
    public static void main(String[] args) {
        // 변수 value 선언
        int value = 10 ;

        // 연산 결과를 변수 result의 초기값으로 대입
        int result = value + 10;

        // 변수 rerult 값을 읽고 콘솔에 출력
        System.out.println(result);


    }

}


